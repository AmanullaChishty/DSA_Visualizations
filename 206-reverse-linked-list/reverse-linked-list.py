# Definition for singly-linked list.
# class ListNode:
#     def __init__(self, val=0, next=None):
#         self.val = val
#         self.next = next
class Solution:
    def reverseList(self, head: Optional[ListNode]) -> Optional[ListNode]:
        prev = None
        curr = head

        while curr:
            next_node = curr.next #Saving 
            curr.next = prev #Reversing

            prev = curr #Advancing
            curr = next_node #Advancing
        
        return prev

        